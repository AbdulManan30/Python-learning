# Arithmetic Operators
a = 10
b = 5
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)
print(a ** b)
print(a // b)

# Assignment Operators
a = 10
b = 5
a += b
a -= b
print(a)

# Comparison Operators
a = 10
b = 5
print(a == b)
print(a != b)
print(a >= b)
print(a <= b)

# Logical Operators
a = True
b = False
print(a and b)
print(a or b)
print(not a)
